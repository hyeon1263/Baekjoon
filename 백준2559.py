# 수열 

N, k = map(int, input().split())
temp = list(map(int, input().split()))

total = sum(temp[:k])
best = total
for i in range(len(temp)-k):
    total -= temp[i]
    total += temp[k+i]
    if total > best:
        best = total
print(best)